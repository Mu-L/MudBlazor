namespace MudBlazor;

/// <summary>
/// The position of the pager for a <see cref="MudTable{T}"/> component.
/// </summary>
public enum PagerPosition
{
    /// <summary>
    /// Display the pager at the bottom of the table.
    /// </summary>
    Bottom,

    /// <summary>
    /// Display the pager at the top of the table.
    /// </summary>
    Top,

    /// <summary>
    /// Display the pager at both the top and bottom of the table.
    /// </summary>
    TopAndBottom
}
